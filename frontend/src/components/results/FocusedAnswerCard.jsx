import React from 'react';
import { Lightbulb, AlertCircle, CheckCircle, Users } from 'lucide-react';
import { motion } from 'framer-motion';

export default function FocusedAnswerCard({ result }) {
  const fa = result?.focused_answer;
  if (!fa) return null;

  const directAnswer    = fa.direct_answer || result.recommendation || '';
  const evidenceSummary = fa.disease_specific_evidence || '';
  const keyFindings     = Array.isArray(fa.key_findings) ? fa.key_findings : [];
  const safetyNotes     = fa.safety_notes || '';
  const researchers     = Array.isArray(fa.researchers) ? fa.researchers : (result.topResearchers || []);
  const disclaimer      = result.disclaimer || 'Information generated by AI. Consult a doctor.';

  return (
    <motion.div
      initial={{ opacity: 0, y: 6 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.2 }}
      className="space-y-3 mt-3"
    >
      {}
      {directAnswer && (
        <div className="rounded-xl p-4" style={{ background: '#eff6ff', border: '1px solid #bfdbfe' }}>
          <div className="flex items-center gap-2 mb-2">
            <Lightbulb size={13} style={{ color: '#2563eb' }} />
            <p className="text-[10px] font-semibold uppercase tracking-widest" style={{ color: '#2563eb' }}>
              Direct Answer
            </p>
          </div>
          <p className="text-sm leading-relaxed" style={{ color: '#1a1a1a' }}>{directAnswer}</p>
        </div>
      )}

      {}
      {evidenceSummary && (
        <div className="rounded-xl p-4" style={{ background: '#f0fdf4', border: '1px solid #bbf7d0' }}>
          <div className="flex items-center gap-2 mb-2">
            <CheckCircle size={13} style={{ color: '#059669' }} />
            <p className="text-[10px] font-semibold uppercase tracking-widest" style={{ color: '#059669' }}>
              Disease-Specific Evidence
            </p>
          </div>
          <p className="text-sm leading-relaxed" style={{ color: '#1a1a1a' }}>{evidenceSummary}</p>
        </div>
      )}

      {}
      {keyFindings.length > 0 && (
        <div className="rounded-xl p-4" style={{ background: '#f0f9ff', border: '1px solid #bae6fd' }}>
          <p className="text-[10px] font-semibold uppercase tracking-widest mb-3" style={{ color: '#0891b2' }}>
            Supporting Evidence
          </p>
          <div className="space-y-2">
            {keyFindings.map((item, i) => (
              <div key={i} className="flex gap-2.5 items-start">
                <span className="flex-shrink-0 w-5 h-5 rounded-full flex items-center justify-center text-[10px] font-bold text-white mt-0.5"
                  style={{ background: 'linear-gradient(135deg,#0891b2,#3b82f6)' }}>
                  {i + 1}
                </span>
                <p className="text-sm leading-relaxed flex-1" style={{ color: '#1a1a1a' }}>{item}</p>
              </div>
            ))}
          </div>
        </div>
      )}

      {}
      {safetyNotes && (
        <div className="rounded-xl px-4 py-3 flex gap-2.5"
          style={{ background: '#fffbeb', border: '1px solid #fde68a' }}>
          <AlertCircle size={13} style={{ color: '#d97706', flexShrink: 0, marginTop: '2px' }} />
          <div>
            <p className="text-[10px] font-semibold uppercase tracking-widest mb-1" style={{ color: '#b45309' }}>
              Safety Notes
            </p>
            <p className="text-xs leading-relaxed" style={{ color: '#92400e' }}>{safetyNotes}</p>
          </div>
        </div>
      )}

      {}
      {researchers.length > 0 && (
        <div className="rounded-xl p-4" style={{ background: '#faf5ff', border: '1px solid #e9d5ff' }}>
          <div className="flex items-center gap-2 mb-3">
            <Users size={13} style={{ color: '#7c3aed' }} />
            <p className="text-[10px] font-semibold uppercase tracking-widest" style={{ color: '#7c3aed' }}>
              Key Researchers
            </p>
          </div>
          <div className="space-y-2">
            {researchers.slice(0, 5).map((r, i) => (
              <div key={i} className="flex gap-2 items-start">
                <span className="flex-shrink-0 w-5 h-5 rounded-full flex items-center justify-center text-[10px] font-bold text-white mt-0.5"
                  style={{ background: 'linear-gradient(135deg,#7c3aed,#3b82f6)' }}>
                  {i + 1}
                </span>
                <div>
                  <p className="text-sm font-medium" style={{ color: '#1a1a1a' }}>
                    {typeof r === 'string' ? r : r.name}
                  </p>
                  {r.paperCount > 0 && (
                    <p className="text-xs" style={{ color: '#6b7280' }}>
                      {r.paperCount} paper{r.paperCount !== 1 ? 's' : ''}
                      {r.totalCitations > 0 && ` · ${r.totalCitations.toLocaleString()} citations`}
                    </p>
                  )}
                </div>
              </div>
            ))}
          </div>
        </div>
      )}

      {}
      <div className="rounded-xl px-4 py-3 flex gap-2.5"
        style={{ background: '#fffbeb', border: '1px solid #fde68a' }}>
        <AlertCircle size={13} style={{ color: '#d97706', flexShrink: 0, marginTop: '2px' }} />
        <p className="text-xs leading-relaxed" style={{ color: '#92400e' }}>{disclaimer}</p>
      </div>
    </motion.div>
  );
}
